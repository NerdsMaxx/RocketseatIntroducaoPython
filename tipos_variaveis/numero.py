# inteiro 
numero_inteiro = 3
print(f'numeroInteiro: {numero_inteiro}')

# real com ponto flutuante
numero_real = 3.14
print(f'numeroReal: {numero_real}')

print(f'Tipo variável inteiro: {type(numero_inteiro)}')
print(f'Tipo variável real: {type(numero_real)}')

# soma +
soma = 1 + 1
print(f'1 + 1 = {soma}')
print(f'Resultado float: {float(soma)}')

# substracao -
substracao = 1 - 1
print(f'1 - 1 = {substracao}')

# multiplicacao *
multiplicacao = 2 * 2
print(f'2 * 2 = {multiplicacao}')

# divisao /, sempre vai retornar float
divisao = 5 / 2
print(f'5 / 2 = {divisao}')
print(f'Tipo variável divisao: {type(divisao)}')
print(f'Resultado inteiro: {int(divisao)}')

# divisao //, sempre vai retornar int quando é duas barras
divisaoInteira = 5 // 2
print(f'5 // 2 = {divisaoInteira}')


# modulo %
modulo = 5 % 2
print(f'5 % 2 = {modulo}')

# potencia **
potencia = 2 ** 3
print(f'2 ** 3 = {potencia}')